                            <h2 class="section-title">Principles of our Teachers </h2>
                            <p>Our teachers will adhere to the Eight Responsibilities outlined by Imam al-Ghazālī ra:
                            </p>

                            <ol>
                                <li>To teach for the sake of God, alone.</li>
                                <li>To behave as a parent would behave towards their very own children.</li>
                                <li>To advise the students with prudence.</li>
                                <li>To advise the students against learning too quickly.
                                </li>
                                <li>To advise students to conquer the urge to overtake ones classmates.</li>
                                <li>He should reprimand with moderation, in private, discreetly and not in public.
                                </li>
                                <li>Be wary of consistent rebuke.</li>
                                <li>To actively pursue that which he/she teaches and that his actions do not contravene
                                    that
                                    which he/she is trying to inculcate in the students.
